<h2>Política de Fretes</h2>

<h2>Prazos de entrega</h2>

<p>Durante o processo de compra, nosso site informará uma estimativa de prazo para a entrega dos seus produtos. Essa estimativa leva em conta os produtos adquiridos, as suas quantidades, o estoque disponível e a distância entre nosso local de distribuição e o endereço de entrega informado pelo cliente. A entrega é terceirizada, através de transportadoras e Correios.</p>

<p>O prazo de entrega em dias úteis começa a ser contado a partir do dia seguinte à postagem do pedido, de forma que o dia em que o pedido é postado não entra na contagem. Esse prazo é determinado exclusivamente pela transportadora ou pelos Correios, não tendo a %nome do site% qualquer relação com a definição ou possíveis alterações nele, podendo inclusive ocorrer atrasos por motivos de força maior fora do controle da %nome do site%.</p>

<h2>Valor do frete</h2>

<p>O valor do frete da entrega dos produtos é calculado automaticamente pelo site no momento da compra ou na página do produto, a partir do peso, volume e quantidade dos produtos adquiridos, assim como da distância entre nosso local de distribuição e o local de entrega informado pelo cliente.</p>

<h2>Condições da entrega</h2>

<p>Todos os pedidos serão processados de acordo com a forma escolhida pelo cliente, em até 1 dias útil após a confirmação do pagamento.</p>

<h3>Prazo médio de entrega</h3>

<p>O prazo médio estimado de entrega dos produtos é de 1 a 10 dias úteis, podendo haver variações no prazo por parte dos Correios ou da Transportadora e possíveis problemas de greves dos trabalhadores dos Correios, além de outros fatores alheios ao nosso controle.</p>

<p>Esses são os prazos máximos, mas com base em nossos em nossos dados, 96% de nossos pedidos foram entregues antes do prazo estipulado.</p>

<h3>Rastreamento</h3>

<p>Todas as compras possuem um código de rastreamento que será enviado ao e-mail do cliente no momento do envio. Com ele, o pedido pode ser acompanhado desde a saída do CD de origem até a chegada em sua residência.</p>

<h3>Retirada em agência</h3>

<p>A entrega é sempre feita pelos Correios e Transportadoras, porém, em alguns casos, como destinatário ausente, regiões com restrição de entrega, entre outros motivos, os Correios ou a Transportadora poderão deixar um aviso para retirar os produtos na agência mais próxima do endereço fornecido.</p>

<h3>Endereço de entrega</h3>

<p>Pedimos atenção no preenchimento do endereço de entrega para garantir que a mesma seja feita dentro do prazo estabelecido. Lembrando que o mesmo não poderá ser alterado após 24 horas de efetuado o pedido no site, pois dentro desse prazo já é feito o processamento do pedido para envio, e também não será possível fazer o cancelamento da compra após 24 horas.</p>

<h3>Informações de rastreamento</h3>

<p>Por se tratar de encomenda, as informações no rastreamento no site dos Correios poderão ocorrer entre 1 a 3 dias úteis após a informação do código de rastreamento. Produtos danificados durante o transporte são da responsabilidade dos serviços postais.</p>

<h3>Responsabilidades</h3>

<p>O preenchimento do endereço de entrega deverá ser revisado pelo comprador antes da confirmação de compra, e havendo devolução do produto por endereço incorreto ou dados insuficientes a repostagem será de inteira responsabilidade do cliente.</p>

<p>Em caso de tentativa de entrega pelos Correios sem resultado, ficando na agência à disposição para ser retirada, mas não sendo, será devolvida para o remetente, o reenvio é de responsabilidade do cliente.</p>

