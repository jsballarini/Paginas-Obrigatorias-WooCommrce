<h2>Política de Trocas e Devoluções</h2>

<h2>1. Entrega das mercadorias</h2>

<p>1.1. A %nome do site% utiliza diferentes serviços para a entrega de seus produtos e o tempo de transporte pode variar de acordo com a região do destinatário. Por isso, esteja atento com o status de seu pedido e se certifique de que haverá alguém apto para receber as encomendas na data prevista entre às 7h e 19h.</p>

<p>1.2. Caso a entrega não seja efetivada, haverá mais 02 (duas) tentativas posteriores. Contudo, se mesmo assim a entrega não for concluída, o produto voltará ao Centro de Distribuição da %nome do site% e o SAC da empresa entrará em contato com o cliente para que possam decidir os próximos passos.</p>

<p>1.3. Caso ocorra insucesso na reentrega do pedido, após a 03 (terceira) tentativas, será cobrado novamente o frete do cliente.</p>

<p>1.4. Todas as encomendas a serem entregues estarão em caixas, stretchadas ou ensacadas, lacrados com fita. Caso o consumidor constate que a embalagem foi violada, a recomendação é que esta seja recusada no momento da entrega, evitando assim posteriores transtornos.</p>

<p>1.5. A %nome do site% efetua a entrega somente no endereço indicado pelo cliente no ato do pedido. Os entregadores não estão autorizados a instalar, montar, desmontar, mover, ligar, testar ou realizar qualquer tarefa que implique em manuseio dos produtos entregues, sob qualquer hipótese. Entregas em condomínios serão efetuadas na portaria central. Caso o condomínio autorize, a entrega poderá ser feita na própria casa do cliente ou portaria do seu prédio. Em todos os casos, a entrega diretamente no apartamento não é permitida.</p>

<p>1.6. Caso seja constatado que o(s) produto(s) está(ão) em desacordo com o que foi adquirido, o cliente deverá recusar o recebimento do pedido, registrar o motivo e comunicar a Central de Atendimento ao Cliente da %nome do site% sobre o ocorrido.</p>

<h2>2. Horários de Entrega</h2>

<p>Para pedidos efetuados pelo televendas/whatsapp, entregue por Motoboy na região de Porto Alegre, o cliente poderá optar pela entrega de manhã (09: as 12:00) ou a tarde (14:00 as 18:00), não sendo possível a marcação de horário personalizado. Essa modalidade funciona desta maneira por se tratar de entrega terceirizada formada por rotas. Caso o cliente precise de um horário exclusivo deverá contatar o televendas para simular o custo da entrega.</p>

<h2>3. Troca de produtos com defeito</h2>

<p>3.1. A %nome do site% efetuará a troca do(s) produto(s) adquirido(s) por seus Clientes de acordo com o previsto no Código de Defesa do Consumidor.</p>

<p>3.2. A opção de troca do produto somente estará disponível na hipótese de constatação de vício ou defeito, devendo o Cliente comunicar a Central de Atendimento ao Cliente e seguir as instruções recebidas para que o pedido de troca seja analisado e processado.</p>

<p>3.3. Caso identificado defeito técnico ou de fabricação, o prazo para solicitação de troca/devolução é de até 7 (sete) dias corridos, contando da data do recebimento do produto. Após esse período, o Cliente deverá encaminhar o produto à assistência técnica do fabricante.</p>

<p>3.4. Caso haja a entrega de produtos diversos aos originalmente adquiridos, o consumidor deverá recusar a entrega ou solicitar troca do produto em até 7 (sete) dias corridos a contar da data do seu recebimento.</p>

<p>3.5. Para efetuar o procedimento de troca com sucesso o Cliente deverá entrar em contato com o Serviço de Atendimento ao Cliente para obter o passo a passo a ser seguido.</p>

<h3>3.6. Opções de troca</h3>

<p>Caso o produto a ser trocado não esteja mais disponível, o Cliente poderá optar por:</p>
<ul>
    <li>a) Vale Troca para utilização em uma próxima compra, incluindo o valor do frete, caso tenha sido cobrado;</li>
    <li>b) Troca por outro produto disponível no %site%, do mesmo valor do produto adquirido, incluindo o frete, caso tenha sido cobrado;</li>
    <li>c) Restituição do valor do produto.</li>
</ul>

<p>3.7. Produtos avariados (saco rasgado, caixa danificada, produto incorreto) deverá ser recusado ou colocada alguma observação no conhecimento de entrega, para que a %nome do site% entre em contato com transportador e resolva o ocorrido.</p>

<h2>4. Arrependimento da compra (devolução)</h2>

<p>4.1. Caso o Cliente se arrependa de uma compra efetuada, poderá exercer seu direito ao manifestar seu interesse ao Serviço de Atendimento ao Cliente no prazo de 07 (sete) dias corridos a contar do recebimento do produto.</p>

<p>4.2. O pedido de desistência passará por uma avaliação para verificar se houve utilização do produto ou sua destruição, ainda que parcial. Nestes casos, a devolução não será possível.</p>

<p>4.3. Os produtos deverão ser enviados em suas embalagens originais com todos os manuais e acessórios.</p>

<p>4.4. Constatado o cancelamento da compra, a %nome do site% adotará as providências e os valores serão restituídos ao Cliente de acordo com o meio de pagamento utilizado para realização da compra: sendo (i) depósito em conta corrente, no prazo de até 7 (sete) dias úteis, ou (ii) acionamento da administradora de cartão de crédito, no prazo de 7 (sete) dias úteis, para que ela comunique a instituição financeira responsável pelo crédito (estorno) que poderá ocorrer em até duas faturas subsequentes, conforme a data de fechamento da fatura de cartão de crédito.</p>

<p>4.5. Os prazos só serão computados a partir da devolução do(s) produto(s), observados os critérios estabelecidos no item 6, subitem 6.3.</p>

<p>4.6 Para casos de arrependimento de compra a %nome do site% não se obriga a ressarcir o frete dos produtos, tendo em vista que esse custo é pago a empresa terceirizada.</p>

